USE tpcc
CREATE TABLE CUSTOMER (
  C_W_ID INT ,
  C_D_ID INT ,
  C_ID INT ,
  C_DISCOUNT DECIMAL(4,4) ,
  C_CREDIT CHAR(2) ,
  C_LAST VARCHAR(16) ,
  C_FIRST VARCHAR(16) ,
  C_CREDIT_LIM DECIMAL(12,2) ,
  C_BALANCE DECIMAL(12,2) ,
  C_YTD_PAYMENT FLOAT ,
  C_PAYMENT_CNT INT ,
  C_DELIVERY_CNT INT ,
  C_STREET_1 VARCHAR(20) ,
  C_STREET_2 VARCHAR(20) ,
  C_CITY VARCHAR(20) ,
  C_STATE CHAR(2) ,
  C_ZIP CHAR(9) ,
  C_PHONE CHAR(16) ,
  C_SINCE TIMESTAMP  DEFAULT CURRENT_TIMESTAMP,
  C_MIDDLE CHAR(2) ,
  C_DATA VARCHAR(500) ,
  PRIMARY KEY (C_W_ID,C_D_ID,C_ID)
);


CREATE TABLE DISTRICT (
  D_W_ID INT ,
  D_ID INT ,
  D_YTD DECIMAL(12,2) ,
  D_TAX DECIMAL(4,4) ,
  D_NEXT_O_ID INT ,
  D_NAME VARCHAR(10) ,
  D_STREET_1 VARCHAR(20) ,
  D_STREET_2 VARCHAR(20) ,
  D_CITY VARCHAR(20) ,
  D_STATE CHAR(2) ,
  D_ZIP CHAR(9) ,
  PRIMARY KEY (D_W_ID,D_ID)
);

